# Setup signal handlers
shutdown_mariadb() {
    echo "üõë Recibida se√±al de shutdown..."
    
    # Try graceful shutdown first
    if [ -n "${MYSQL_ROOT_PASSWORD}" ]; then
        mysqladmin -uroot -p"${MYSQL_ROOT_PASSWORD}" shutdown 2>/dev/null || true
    fi
    
    # Send SIGTERM to MariaDB
    kill -TERM $MARIADB_PID 2>/dev/null || true
    
    # Wait up to 30 seconds
    for i in {1..30}; do
        if ! kill -0 $MARIADB_PID 2>/dev/null; then
            echo "‚úÖ MariaDB detenido correctamente"
            exit 0
        fi
        sleep 1
    done
    
    # Force kill if still running
    echo "‚ö†Ô∏è  Forzando shutdown..."
    kill -KILL $MARIADB_PID 2>/dev/null || true
    exit 1
}

trap shutdown_mariadb SIGTERM SIGINT

# Wait for MariaDB process
echo "‚úÖ Sistema listo - MariaDB corriendo"
wait $MARIADB_PID
EXIT_CODE=$?

echo "‚ÑπÔ∏è  MariaDB termin√≥ con c√≥digo: $EXIT_CODE"
exit $EXIT_CODE
