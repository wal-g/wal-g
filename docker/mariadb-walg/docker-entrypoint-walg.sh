        # Fix permissions
        echo "üîí Aplicando permisos $MYSQL_USER:$MYSQL_GROUP..."
        chown -R "$MYSQL_USER:$MYSQL_GROUP" "$DATADIR"
        chmod 750 "$DATADIR"
        
        echo "‚úÖ Backup restaurado exitosamente"
