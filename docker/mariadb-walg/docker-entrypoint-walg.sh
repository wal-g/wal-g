    # Stop binary logging to prevent new logs during replay
    mysql -e "SET GLOBAL log_bin=OFF;" 2>/dev/null || true
    
    # Execute binlog-replay
    echo "üîÑ Aplicando binlogs hasta $WALG_PITR_UNTIL..."
